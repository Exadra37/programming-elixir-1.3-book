In Elixir there is no fixed limit for the size of integers.

Integer literals can be represented as:
	
	* decimal - 1234
	* hexadecimal - 0xcafe
	* octal - 0o765
	* binary - 0b1010

On decimal integers we can use underscores to separate groups of digits, like 1_000_000

